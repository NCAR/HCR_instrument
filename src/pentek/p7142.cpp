#include "p7142.h"
#include <fcntl.h>

using namespace Pentek;

////////////////////////////////////////////////////////////////////////////////////////
p7142::p7142(std::string devName):
_ok(true),
_devName(devName)
{

	_ok = false;
	 _devCtrl = devName + "/ctrl";
	int fd = open(_devCtrl.c_str(), O_RDWR);
	if (fd < 0)
		return;
	close(fd);
	_ok = true;
}

////////////////////////////////////////////////////////////////////////////////////////
p7142::~p7142() {
}

////////////////////////////////////////////////////////////////////////////////////////
bool
p7142::ok() {
	return _ok;
}

